          <article class="article-content">
              <p>We started Hooros with the idea of creating a platform that allows for freelancers to work with corporates within a trusted environment in order to ensure you, as the freelancer, are adequately compensated for the work you complete. More importantly, we want to provide you with projects that will be challenging enough to help you improve your skill-sets and advance your professional career.</p>

    	      <h2>Using Hooros is easy</h2>

     		  <p>All you have to do, after your profile has been verified, is find a project you would like to be involved in and simply wait. If the client is interested in working with you, we will notify you.</p>

              <h2>Work and Profit</h2>

              <p>If a client approves your request to work on a project, the next steps are fairly easy, work, complete your work and profit. No bosses, no suits, no management, we will ask for status updates to ensure you are not running into any problems and to help where we can, but ultimately you will have full control of your working environment and working hours. This is how work is supposed to be!</p>

              <h2>Revolution</h2>

              <p>Ultimately we believe that the 9-5 is an outdated model with no functional purpose. Work is supposed to be done within the hours you are most comfortable with, within an environment you feel most comfortable working in and without the drama that comes along with office politics. We do understand however, that companies have a natural aversion towards the idea of working with freelancers because companies are built on principals that go against the idea of individualistic work. We believe this is where freelance marketplaces tend to fail, instead we offer accountability to our clients, guaranteeing regular updates and progress completion and we ensure that there is clear communication of expectations between you and the client, leaving you to focus on what you love the most- your work.</p>
